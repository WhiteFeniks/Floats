#include "../includes/libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *new_ptr;
	t_list *buf;

	new_ptr = *alst;
	while (new_ptr != NULL)
	{
		buf = new_ptr;
		ft_lstdelone(&new_ptr, del);
		new_ptr = buf->next;
	}
	*alst = NULL;
}
